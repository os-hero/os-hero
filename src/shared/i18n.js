function normalizeLanguage(value) {
  return LANGUAGE_OPTIONS.some((language) => language.id === value) ? value : DEFAULT_LANGUAGE;
}

function getMessages(language) {
  const normalized = normalizeLanguage(language);
  return {
    ...DICTIONARY[DEFAULT_LANGUAGE],
    ...DICTIONARY[normalized]
  };
}

function formatMessage(template, values) {
  if (!values) {
    return template;
  }

  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_match, key) => {
    return Object.prototype.hasOwnProperty.call(values, key) ? String(values[key]) : `{${key}}`;
  });
}

function translate(language, key, values) {
  const messages = getMessages(language);
  return formatMessage(messages[key] || key, values);
}

